def get_deeplabv3plus(
        ckpt_path: str = "models/checkpoints/best_deeplabv3plus_resnet50_voc_os16.pth",
        classes: int = 19,
        **kwargs):
    model = deeplabv3plus_resnet50(num_classes=classes, output_stride=16)
    if ckpt_path:
        state_dict = torch.load(ckpt_path)['model_state']
        model.load_state_dict(state_dict)
    return model

 
